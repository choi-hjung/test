public class MenuItem {
    // 속성
    // 햄버거의 이름, 가격, 설명
    private String name;
    private double cost;
    private String info;

    // 생성자
    public MenuItem(String name, double cost, String info) {
        this.name = name;
        this.cost = cost;
        this.info = info;
    }



}
